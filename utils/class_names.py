# 基础类别数量（当前主模型的输出类别数）
BASE_NUM_CLASSES = 59  # 0-58共59个类别

# 工具函数：获取激活类别名称文件路径
def _active_class_names_path() -> str:
    """
    获取激活类别名称文件的存储路径

    文件存放在models目录下，与主模型文件一起版本化管理。

    返回：
    - str: 文件路径 "models/active_class_names.json"
    """
    # 存放在 models/ 下，和主模型一起版本化
    return "models/active_class_names.json"

# 获取基础类别名称列表
def _base_class_names_list():
    """
    获取基础类别名称列表

    只返回0到BASE_NUM_CLASSES-1的类别名称，
    确保与当前主模型输出维度一致。

    返回：
    - list: 基础类别名称列表
    """
    # 只取 0..BASE_NUM_CLASSES-1，确保与当前主模型输出维度一致
    return [CLASS_NAMES[i] for i in range(BASE_NUM_CLASSES)]

# 加载激活的类别名称列表
def load_active_class_names():
    """
    读取当前激活模型对应的类别名称列表

    从JSON文件中加载类别名称列表，按class_id的顺序排列。
    如果文件不存在，默认使用基础59类。

    支持增量学习：当有新的病害类别通过学习中心添加时，
    类别列表会动态扩展。

    返回：
    - list: 类别名称列表，按类别ID顺序排列
    """
    import json
    import os

    path = _active_class_names_path()
    if os.path.exists(path):
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
        if isinstance(data, list) and len(data) >= BASE_NUM_CLASSES:
            return data

    # 首次启动时创建默认文件，便于后续增量训练继续追加
    os.makedirs("models", exist_ok=True)
    base = _base_class_names_list()
    with open(path, "w", encoding="utf-8") as f:
        json.dump(base, f, ensure_ascii=False, indent=2)
    return base

# 根据类别ID获取类别名称
def get_class_name(class_id):
    """
    根据类别ID获取对应的类别名称

    支持增量训练的动态类别扩展，当类别ID超出基础类别范围时，
    返回动态添加的新类别名称。

    参数：
    - class_id: 类别ID（整数或字符串）

    返回：
    - str: 对应的类别名称，如果ID无效则返回"未知类别"
    """
    names = load_active_class_names()
    if 0 <= int(class_id) < len(names):
        return names[int(class_id)]
    return f"未知类别({class_id})"